// WinRunShared - Shared types and utilities for WinRun host components
//
// This module provides common types used across WinRun host targets:
// - VMConfiguration.swift: VM configuration, validation, and state types
// - Logging.swift: Logger protocol and StandardLogger
// - Errors.swift: WinRunError and error handling
// - XPCAuth.swift: XPC authentication and request throttling
// - InputModels.swift: Mouse, keyboard, clipboard, and drag/drop events
// - SpiceMetrics.swift: Spice stream performance metrics
//
// All types are re-exported from their respective files via Swift's
// module system. Import WinRunShared to access all shared types.
